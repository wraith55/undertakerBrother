			Throughout the course of the Undertaker’s career, there is one accomplishment that stands
			above the rest. Starting in 1991 with a win against “Superfly” Jimmy Snuka, the Undertaker
			amassed an unprecedented 21 match winning streak at Wrestlemania. For over two decades,
			nearly two dozen superstars attempted to end the streak in a variety of ways, including a 2 on 1
			match, Hell in a Cell cages, and casket matches. Ultimately, it was the Undertaker’s old nemesis
			Brock Lesnar that ended the Undertaker’s winning ways at Wrestlemania 30. Three years later,
			in 2017, Roman Reigns would join the exclusive club of defeating the Undertaker at
			Wrestlemania, becoming number two in 23 and 2.
			His match against Reigns didn’t just signal a defeat for the Undertaker though. Following his
			match, the Undertaker removed his gloves and left them in the middle of the ring, and, as of this
			writing, hasn’t been seen since, perhaps signaling an end to the greatest career in professional
			wrestling history.</main>
